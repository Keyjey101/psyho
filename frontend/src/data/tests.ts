// Каталог психологических тестов.
//
// Все тесты — мультивыбор: пользователь выбирает один вариант на вопрос,
// варианту соответствует целочисленный балл. Итоговая интерпретация —
// диапазон суммы баллов в `interpretations`.
//
// Это популярные публично доступные методики (PHQ-9, GAD-7, PSS-10, и т.д.)
// в адаптированном русскоязычном виде, исключительно для самоисследования.
// Это НЕ диагноз — у каждого результата явно проговаривается, что результаты
// нужно обсуждать со специалистом.

export type TestCategory =
  | "Депрессия"
  | "Тревога"
  | "Стресс"
  | "Самооценка"
  | "Одиночество"
  | "Выгорание"
  | "Прокрастинация"
  | "Самосострадание"
  | "Жизнестойкость"
  | "Счастье"
  | "Перфекционизм"
  | "Сон"
  | "Отношения"
  | "Осознанность"
  | "Границы";

export interface TestOption {
  label: string;
  /** Балл, добавляемый к итогу. */
  score: number;
}

export interface TestQuestion {
  text: string;
  /** Если true — ответы оцениваются «обратно»: первый вариант = max-балл. */
  reverse?: boolean;
  options: TestOption[];
}

export interface TestInterpretation {
  /** Включающие границы по сумме баллов. */
  minScore: number;
  maxScore: number;
  level: string;
  /** Эмодзи / маркер для UI. */
  marker: string;
  description: string;
  /** Что предложить дальше — короткий, поддерживающий совет. */
  advice: string;
}

export interface PsyTest {
  id: string;
  title: string;
  shortTitle: string;
  category: TestCategory;
  /** Краткое описание для карточки в каталоге. */
  description: string;
  /** Чем именно может помочь этот тест. */
  about: string;
  /** Источник методики. */
  source: string;
  emoji: string;
  /** Цвет карточки/бейджа категории. */
  accent: string;
  questions: TestQuestion[];
  interpretations: TestInterpretation[];
  /** Дисклеймер показывается перед стартом и в результате. */
  disclaimer?: string;
  /**
   * Направление "лучше". true — для скрининговых шкал, где высокий балл
   * означает больше симптомов (PHQ-9, GAD-7 и т.п.). false — для шкал
   * благополучия и ресурсов (самосострадание, жизнестойкость, счастье).
   */
  lowerIsBetter: boolean;
}

