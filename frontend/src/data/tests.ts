export function getTest(id: string): PsyTest | undefined {
  return TESTS.find((t) => t.id === id);
}

export function maxPossibleScore(test: PsyTest): number {
  return test.questions.reduce(
    (sum, q) => sum + Math.max(...q.options.map((o) => o.score)),
    0,
  );
}

export function minPossibleScore(test: PsyTest): number {
  return test.questions.reduce(
    (sum, q) => sum + Math.min(...q.options.map((o) => o.score)),
    0,
  );
}

export function getInterpretation(
  test: PsyTest,
  totalScore: number,
): TestInterpretation {
  for (const interp of test.interpretations) {
    if (totalScore >= interp.minScore && totalScore <= interp.maxScore) {
      return interp;
    }
  }
  // Fallback — never happens with well-formed tests, but keeps types honest.
  return test.interpretations[test.interpretations.length - 1];
}
